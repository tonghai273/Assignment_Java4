/**
 *
 * @author Tong Duy Hai
 */
public class quanLySanPham extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		String maSP  = request.getParameter("masanpham");
		HttpSession session = request.getSession();
		Object ad =  session.getAttribute("ADMIN");
		if(ad!=null){
                        new gioHangDao().xoaSP(maSP);
			response.sendRedirect("capNhatSanPham.jsp");
			
		}else{
			response.sendRedirect("QuyenAdmin.jsp");
		}
		
	}

	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		doGet(request, response);
	}

}
